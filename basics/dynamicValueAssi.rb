# Ruby Program for Dynamic Value Assignment and Type Checking

x = 10
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = 10.10
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = "kavit"
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = true
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = false
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = [10, 20, 30, 40, 50]
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = nil
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

x = {name: "kavit", age: 24, city: "rajkot"}
puts "value of x :: #{x}"
puts "data type of x :: #{x.class}"

# value of x :: 10
# data type of x :: Integer
# value of x :: 10.1
# data type of x :: Float
# value of x :: kavit
# data type of x :: String
# value of x :: true
# data type of x :: TrueClass
# value of x :: false
# data type of x :: FalseClass
# value of x :: [10, 20, 30, 40, 50]
# data type of x :: Array
# value of x ::
# data type of x :: NilClass
# value of x :: {:name=>"kavit", :age=>24, :city=>"rajkot"}
# data type of x :: Hash